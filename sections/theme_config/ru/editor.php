<h3>Редактор файлов (Template Editor)</h3>

<p>При клике на эту опцию откроется онлайн-редактор файлов шаблона. Вы можете по своему усмотрению редактировать файлы
    разметки (файлы с расширением <b>.liquid</b>), скрипты (<b>javascript</b>), стили (<b>css</b>) и файл настроек (<b>settings.html</b>).
</p>
