<h4>Страница оплаты (Checkout)</h4>

<p>В этом разделе представлены настройки оформления внешнего вида страницы заказа. Вы можете настроить баннер, логотип,
    заголовок страницы, а также цвета кнопок.</p>

<h4>Additional Layouts</h4>

<p>Управление дополнительными макетами страниц. Добаить, удалить или редактировать макеты страниц вы можете в меню
    <strong>Themes > Template Editor > Layouts</strong>.</p>