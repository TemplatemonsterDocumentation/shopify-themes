<h4>Mailing list
    <span class="muted">(опционально)</span>
</h4>

<p>Раздел содержит инструменты настройки почтовых рассылок.</p>

<h4>Payment methods</h4>

<p>Управление отображением иконок-логотипов платёжных систем, которые показываются на странице оформления заказа. Вы
    можете включать/отключать иконки по своему усмотрению.</p>

<h4>Cookies Policy</h4>

<p>В наших шаблонах есть встроенный виджет для отображения информации об использовании <a
        href="http://www.cookielaw.org/the-cookie-law/">Cookie Law</a>. Управление виджетом производится в разделе
    <strong>Cookies Policy</strong>:</p>

<p>Вы можете включить/отключить виджет, редактировать тексты, период в течение которого сообщение не будет отображаться
    (при нажатии кнопки <strong>ОК</strong>), а также ссылку, по которой будет осуществлён переход в случае отказа
    пользователя (при нажатии кнопки <strong>NO</strong>).</p>

<figure class="img-polaroid">
    <img alt="" src="img/06_settings_cookies.png">
