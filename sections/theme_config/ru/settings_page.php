<h3>Основные разделы страницы настроек</h3>

<h4>General</h4>

<p>Здесь вы можете загрузить свой логотип, который будет использоваться в вашем интернет-магазине, а также иконку,
    демонстрируемую в адресной строке браузера (favicon). Также, доступны опции включения/отключения логотипа на
    странице оформления заказа и копирайта в футере.</p>

<h4>Typography</h4>

<p>Раздел настройки типографики шаблона. С помощью представленных опций вы можете выбрать нужный шрифт, а также его цвет
    и размер. Вы можете изменять:</p>

<ul class="marked-list">
    <li>основной шрифт страниц магазина</li>
    <li>текстовые ссылки</li>
    <li>заголовки</li>
    <li>названия продуктов</li>
    <li>описания продуктов</li>
    <li>цена продуктов</li>
</ul>

<p>Вы можете использовать как системные шрифты, так и <a href="//www.google.com/fonts">Google Fonts</a>.</p>

<p class="alert alert-warning"><b>Внимание!</b> Размеры шрифтов необходимо указывать в пикселях (например, <b>12px</b>),
    а цвета — в восьмеричном формате (например, <b>#aabbcc</b>; также, можно выбирать цвета из представленной палитры).
</p>

<h4>Colors</h4>

<p>Управление цветовой схемой шаблона. Цвета необходимо задавать в восьмеричном формате (например, <b>#aabbcc</b>).
    Также, можно выбирать цвета из представленной палитры.</p>

<h4>Gradients
    <span class="muted">(опционально)</span>
</h4>

<p>В этом разделе вы можете редактировать градиенты (если они предусмотрены дизайном шаблона). В наших шаблонах
    используются линейные вертикальные градиенты. Их можно изменять, достаточно задать два цвета, для верхней и нижней
    границ.</p>

<h4>Layout & content settings</h4>

<p>Основные настройки вывода контента. Вы можете:</p>

<ul class="marked-list">
    <li>настроить вывод списка продуктов на главной странице и на странице категории</li>
    <li>управлять поведением боковых колонок (сайдбаров) и их содержимым</li>
</ul>

<h4>Megamenu</h4>

<p>Наши шаблоны поставляются с виджетом Megamenu, с помощью которого вы можете построить мощное и функциональное главное
    меню своего магазина. Виджет имеет множество настроек, которые доступны в соответствующем разделе. Меню
    предусматривает 6 пунктов (<strong>Items</strong>), каждый из которых настраивается в соответствующем подразделе. К
    каждому пункту можно добавить ярлык с текстом (<strong>Item badge</strong>):</p>

<ul class="marked-list">
    <li>обычная настраиваемая ссылка, без субменю.</li>
    <li>каталог продуктов. Задаётся ссылка на перечень всех продуктов, также доступно управление выпадающим
        субменю со списком категорий (<strong>Collections</strong>). Категории для отображения выбираются пользователем.
    </li>
    <li>блог. Блог выбирается пользователем, также доступно включение/отключение выпадающего субменю с
        перечнем последних записей.
    </li>
    <li>продукты со скидками. Задаётся категория, также доступно включение/отключение выпадающего субменю с
        перечнем продуктов из выбранной категории.
    </li>
    <li>обычные настраиваемые ссылки, без субменю.</li>
</ul>

<h4>Slider
    <span class="muted">(опционально)</span>
</h4>

<p>Раздел содержит настройки слайдера, представленного на главной странице вашего интернет-магазина (если слайдер
    предусмотрен дизайном шаблона). Вы можете включать/отключать слайдер, менять изображения, редактировать описания и
    ссылки, на которые ведут слайды.</p>

<p class="alert alert-warning"><b>Внимание!</b> Для сохранения эстетической привлекательности шаблона рекомендуется
    придерживаться размеров изображений, указанных рядом с формой загрузки.</p>

<h4>Custom blocks</h4>

<p>В этом разделе вы можете редактировать баннеры, блоки ссылок, опции социальных аккаунтов, а также другой
    пользовательский контент, представленный в шаблоне. Для редактирования этих блоков не требуются знания вёрстки или
    программирования — настройка сводится к простым действиям, таким как ввод текстов, выбор иконок и загрузка
    изображений.</p>

<h4>Google maps</h4>

<p>Раздел содержит настройки управления отображения гугл-карт на страницах магазина.</p>

<p>Основные настройки:</p>

<p><strong>Адрес. </strong>Задайте почтовый адрес нужного объекта, и виджет отобразит карту этот объект на карте, с
    маркером.</p>

<p><strong>Функции показа карты. </strong>Вы можете включить или отключить отображение карты на главной странице или на
    странице контактов.</p>

