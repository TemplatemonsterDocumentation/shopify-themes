<h3>The main sections on your Theme settings page</h3>

<h4>General</h4>

<p>In this section you can upload your logo image and favicon (the icon that shows up in the address bar of the browser)
    for your online store. There are also options of enabling/disabling the logo on your checkout page and the copyright
    text in the footer area.</p>

<h4>Typography</h4>

<p>The section where you can change the typography settings, such as font family, font size and color. You can modify
    the following settings:</p>

<ul class="marked-list">
    <li>The main font style of your store pages</li>
    <li>Text links</li>
    <li>Titles</li>
    <li>Product names</li>
    <li>Product descriptions</li>
    <li>Product price</li>
</ul>

<p>You can use system fonts as well as <a href="//www.google.com/fonts">Google Fonts</a>.</p>

