<h4>Money options</h4>

<p>If you want to use several currencies in your online store, you can manage them in this section. You should select
    <strong>Show currency selector</strong> and add three-letter currency codes (<a
        href="//www.iso.org/iso/currency_codes">ISO 4217 standard</a>) in the field <strong>Currencies you wish to
        support</strong>.</p>

<h4>Newsletter options</h4>

<p>This section presents the settings for the newsletter subscription forms. Here you can configure the pop-up message
    that prompts you to subscribe (<strong>Newsletter pop-up)</strong>. There many options for the detailed
    configuration where you can change the background image and text to meet your needs.</p>

<p>Also, you can select the form of subscription:</p>

<ul class="marked-list">
    <li>Built-in Shopify form. If you select this option, all subscribers' e-mails will be collected in the section
        <strong>Customers</strong> marked as <strong>Newsletter Subscriber</strong>.
    </li>
    <li>Mailchimp form integration. To work with mailing lists, you can use the Mailchimp service, it is enough to get a
        <a href="https://docs.shopify.com/manual/configuration/store-customization/communicating-with-customers/accounts-and-newsletters/where-do-i-get-my-mailchimp-form-action"
           target="_blank"> subscription form</a> and input it in the <strong>Mailchimp form action URL</strong> field.
    </li>
</ul>

