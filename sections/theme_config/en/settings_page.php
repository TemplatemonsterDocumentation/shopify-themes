<h4>Colors</h4>

<p>Theme color scheme settings. The colors must be specified in hexadecimal format (for example,
    <strong>#aabbcc</strong>). Also, it is possible to select a color from a palette provided.</p>

<h4>Layout & content</h4>

<p>Basic content display settings. You can configure the display of the products list on the home page and on the
    category page, control the layout of the side columns (sidebars) and their contents.</p>

<h4>Gradients
    <span class="muted">(optional)</span>
</h4>

<p>In this section you can edit gradients (if they are available in your theme). We use linear vertical gradients in our
    themes. You can modify them by changing the top and bottom color markers.</p>

<h4>Layout & content settings</h4>

<p>The main settings of your content management. You can do the following:</p>

<ul class="marked-list">
    <li>Select the main navigation of the store (shown in the upper horizontal menu)</li>
    <li>Control the sidebars</li>
    <li>Enable the list of products on the front and category pages</li>
</ul>

<h4>Megamenu</h4>

<p>Our templates include the Megamenu widget which helps to build a powerful and functional main menu of your store. The
    widget has many options that are available in the corresponding section. Menu includes 6 <strong>Items</strong>,
    each of which is configured in the corresponding sub-section. One can add a label with the text (<strong>Item
        badge</strong>) to each item:</p>

<ul class="marked-list">
    <li>the usual custom link, without submenu.</li>
    <li>product catalog. One can set a link to a list of all products, the control of the dropdown submenu
        with the list of categories (<strong>Collections</strong>) is also available. Categories to display are selected
        by
        user.
    </li>
    <li>blog. Blog is chosen by the user, the option to enable/disable the drop-down sub-menu with the list of
        the latest entries is also available.
    </li>
    <li>special products. One can set the category of products to display, the option to enable/disable the
        drop-down sub-menu with the list of the latest entries is also available.
    </li>
    <li>the usual custom links, submenu is not included.</li>
</ul>

<h4>Slider
    <span class="muted">(optional)</span>
</h4>

<p>This section contains the settings of the slider located on the front page (if the slider is included in your
    template design). You can enable/disable the slider, change images, edit description and links assigned to the
    slides.</p>

<p class="alert alert-warning"><b>Attention!</b> To preserve the aesthetic appeal of the theme it is recommended using the image
    dimensions shown next to the Upload button.</p>

<h4>Custom blocks</h4>

<p>In this section you can manage banners, blocks of links, social icons options and other content available in your
    theme. It is not required to have programming skills in order to edit these blocks – configuration is done in easy
    steps like adding text, selecting icons and uploading images.</p>

<h4>Google maps</h4>

<p>This section contains the settings which control the display of Google maps on the pages of the store.</p>

<p>Basic settings:</p>

<p><strong>Address. </strong>Set the mailing address of the desired object, and the widget will display this place on
    the map with a marker.</p>

<p><strong>Function of map to display. </strong>You can enable or disable the display of the map on the home page or
    contact page.</p>

