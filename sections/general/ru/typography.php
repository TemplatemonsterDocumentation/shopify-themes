  </figure>
  
</p>


<h5>Основной шрифт страниц магазина (<strong>base font</strong>)</h5>
    <ol class="marked-list">
<li><strong>Font family</strong> — выберите нужный шрифт из выпадающего списка опции;<p>
  <figure class="img-polaroid">
    <img alt="" src="img/typography-1.png">
  </figure>
  
</p></li>
<li><strong>Font size</strong> — задайте размер шрифта основного текста (от 14 до 16px);
  <figure class="img-polaroid">
    <img alt="" src="img/typography-2.png">
  </figure>
  
  </p>
</li>
<li><strong>Color</strong> — задайте нужный цвет для основного шрифта. Вы можете выбрать цвет из цветовой палитры.<p>
