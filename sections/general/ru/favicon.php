<h3>Настройка иконки сайта (Favicon)</h3>
<p align="justify">С помощью этой опции вы сможете менять иконку вашего сайта (маленькая иконка, которая отображается на  вкладке страницы вашего сайта в браузере). Для иконки используйте любое изображение формата  .png (является наиболее подходящим форматом).</p>


<figure class="img-polaroid">
    <img alt="" src="img/favicon_1.png">
</figure>


  <p>Изображение иконки будет сжато до размеров  32 x 32px.</p>
<p align="justify">У вас есть возможность загрузить изображение для иконки с вашего компьютера, либо выбрать изображение из библиотеки.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/favicon_2.png">
  </figure>
  
</p>
<p>Редактируйте изображение. Для этого выберите <strong>Edit image </strong>из списка опций под изображением.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/favicon_3.png">
  </figure>
</p>
<p align="justify">Напишите краткое описание  изображения, что бы улучшить поисковую оптимизации (SEO) и повысить  уровень доступности изображения для людей со сниженной остротой зрения.</p>
