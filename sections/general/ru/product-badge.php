<h3>Products Badge</h3>
<p>В разделе <strong>Product</strong> включаются и настраиваются бейджи на товарах. Для отображение бейджа новинки необходимо задать количество дней
 после публикации товара, на протяжении которых этот продукт будет отмечен как новый. Для бейджа скидки выбрать тип отображения, 
 в виде размера скидки в процентах либо с текстом &quot;Sale&quot;. </p>

<figure class="img-polaroid">
    <img alt="" src="img/prod_1.png">
  </figure>

<p>Для использования у продуктов цветовой опции необходимо указать название цветовой опции товара на вашем языке.</p>

<figure class="img-polaroid">
    <img alt="" src="img/prod_2.png">
  </figure>

<p>Включить отображение рамки на изображении продукта.</p>

<figure class="img-polaroid">
    <img alt="" src="img/prod_4.png">
  </figure>

<p>Add  &quot;New&quot; and &quot;Sale&quot; badges  to product images for the collection page. </p>

<p>
  <figure class="img-polaroid">
    <img alt="" src="img/product-badge.jpg">
  </figure>
  
</p>
<h5>Let's review the options available:</h5>
<ol class="marked-list">
  <li>To show the labels, you should first enable the <strong>Show badge on product </strong>option.</li>
  <figure class="img-polaroid">
    <img alt="" src="img/product-badge-2.jpg">
  </figure>
  <li><strong>'New' products marker</strong> — enter the number of days the product is considered as &quot;New&quot; (the time period starts from the date the product was added). </li>

<figure class="img-polaroid">
    <img alt="" src="img/product-badge-1.jpg">
  </figure>
  <li><strong>'Sale' marker type</strong> — set the &quot;Sale&quot; label type (Text or Percent). The &quot;Sale&quot; label appears automaticaly after the <strong>Compare at price</strong> is set under the <strong>Products</strong> &gt; <strong>All products</strong> section, in case the <strong>Show badge on product </strong>option  is activated. </li>

<figure class="img-polaroid">
    <img alt="" src="img/product-badge-3.jpg">
  </figure>

</ol>
<p>Remember to <strong>Save</strong> the settings.</p>