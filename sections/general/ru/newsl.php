<h3>Newsletter Popup</h3>
<p>Email newsletter has always been the important part of every marketing strategy. Displays the popup window with subscription form right after the client visits your site.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/newsletter-popup-1.jpg">
  </figure>
  
</p>
<h5>Let's review the options available:</h5>

<p>В разделе Newsletter доступны настройки  функции подписки при помощи Shopify native form или Mailchimp  на выбор.</p>

<figure class="img-polaroid">
    <img alt="" src="img/newsl_1.png">
</figure>

<p>Также  есть возможность включить и настроить всплывающее окно с подпиской, Выбрав его периодичность показа в днях и добавив изображение в качестве фоновой картинки.</p>

<figure class="img-polaroid">
    <img alt="" src="img/newsl_2.png">
</figure>
<ol class="marked-list">
  <li>To display the labels, you should first activate the <strong>Enable newsletter popup </strong>option.</li>
  
  <li><strong>Pop-up disable expiration (in days) — </strong>set the number of days to show the pop-up  again after the user clicks the 'Don't show this message again' button.</li>


  <li><strong>Background image</strong> — download the background image for the newsletter. The image should not exceed 700x400px size.</li>



</ol>
<p>Remember to <strong>Save</strong> the settings.</p>

