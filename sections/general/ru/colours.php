<h3>Colours
</h3>
<p align="justify">В этом разделе вы можете настравать цвета шаблона. Вы можете выбрать цвет из цветовой палитры. Используйте Hex Color Code, чтобы найти нужный цвет. Напр., #58d68d. </p>
<p>
