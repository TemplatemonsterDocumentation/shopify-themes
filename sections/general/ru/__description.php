<h2 class="item4">Настройка шаблона</h2>

<p align="justify">Благодаря множеству опций, вы можете производить тонкую настройку шаблона по своему усмотрению, изменяя гарнитуру,
    кегль и цвет шрифта, цветовую схему, изображения баннеров и слайдера, а также многое другое.</p>

<p>Перейдите к разделу <strong>Online Store &gt; Themes</strong>.</p>
<p>Выберите тему, внешний вид которой хотите изменить. </p>
<p align="justify">При клике на кнопку <strong>Customize theme</strong> откроется страница редактора настроек, с помощью которого вы
можете изменять внешний вид и содержимое шаблона.</p>
