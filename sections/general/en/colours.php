</p>

  <p>In this section, you choose the colors for your site. The   section includes two subsections: <strong>Page colors</strong> and <strong>Color theme</strong>.</p>
<p>Let's explore the options:</p>
  <p>The<strong> Page colors </strong>option controls the <strong>Page background color</strong> and <strong>Border color</strong>.</p>
  <p align="justify">The<strong> Color theme</strong> is used for alternate button styles, some text and icon hover states, and other places.  In this section you can adjust  links, top bar, header, navigation and footer colors.</p>
