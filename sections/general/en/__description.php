<h2 class="item4">Theme Configuration</h2>

<p align="justify">With multiple options you are able to modify the template to your needs. You can edit font family, font color and
size, theme color scheme, banner and slider images, etc.</p>

<p>Navigate to <strong>Online Store &gt; Themes</strong>.</p>
<p>Select the theme you want to modify. </p>
<p>Click <strong>Customize theme</strong> button to open the settings editor page where you can change the appearance
and content of the theme.</p>
