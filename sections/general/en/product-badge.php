<h3>Products Badge</h3>
<p>Add  &quot;New&quot; and &quot;Sale&quot; badges  to product images for the collection page. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/product-badge.jpg">
  </figure>
  
</p>
<h5>Let's review the options available:</h5>
<ol class="marked-list">
  <li>To show the labels, you should first enable the <strong>Show badge on product </strong>option.</li>
  <figure class="img-polaroid">
    <img alt="" src="img/product-badge-2.jpg">
  </figure>
  <li><strong>'New' products marker</strong> — enter the number of days the product is considered as &quot;New&quot; (the time period starts from the date the product was added). </li>

<figure class="img-polaroid">
    <img alt="" src="img/product-badge-1.jpg">
  </figure>
  <li><strong>'Sale' marker type</strong> — set the &quot;Sale&quot; label type (Text or Percent). The &quot;Sale&quot; label appears automatically after the <strong>Compare at price</strong> is set under the <strong>Products</strong> &gt; <strong>All products</strong> section, in case the <strong>Show badge on product </strong>option  is activated. </li>

<figure class="img-polaroid">
    <img alt="" src="img/product-badge-3.jpg">
  </figure>

</ol>
<p>Remember to <strong>Save</strong> the settings.</p>
