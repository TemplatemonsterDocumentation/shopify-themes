      <h5><strong>Add Banner  section</strong></h5>
<figure class="img-polaroid"> <img alt="" src="img/sidebar-31.jpg" /></figure>
        <ol class="index-list">
          <li>To add the Banner to the sidebar, press on the <strong>Add content</strong> and select <strong>Banner </strong>from the dropdown.</li>
          <figure class="img-polaroid"> <img alt="" src="img/sidebar-28.jpg" /></figure>
          <li>The new tab will appear in the <strong>Content</strong> section.</li>
          <li>Click on the<strong> Banner (Title)</strong> tab to configure the section. </li>
          <li>To chose the image for the banner, press the <strong>Select image</strong> button. To upload image, go to the<strong> Settings</strong> &gt; <strong>Files</strong> menu and click <strong>Upload files</strong> button. Note, the image should be more than <strong>270 px </strong>wide.</li>
          <li>Set the banner height (Small/Medium/Large).</li>
          <li>Specify the <strong>Heading</strong> for the banner and add some<strong> Text</strong> you want yo display on the banner.</li>
          <li>You also can attach the <strong>link</strong> to the banner.</li>
          <li> Click <strong>Delete</strong> to remove the section.</li>
        </ol>
