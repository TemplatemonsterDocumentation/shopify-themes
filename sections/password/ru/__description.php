  <a name="1">  </a><h5><strong>How to enable the Password Page</strong></h5>
<p>Navigate to the <strong>Online Store &gt; </strong> <strong>Preferences</strong> section. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/pref.png">
  </figure>
</p>
<p>Go to the<strong> Password page</strong> section.</p>
<p>Tick the <strong>Enable password page</strong> checkbox.</p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/pass.png">
  </figure>
</p


><p>Note, you can change the password here and add the additional information to show  on the Password Page.
</p>
<p><strong>Save</strong> the settings.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/save.png">
  </figure>
</p>
<a name="2">  </a>
<h5><strong>How to customize the Password Page</strong></h5>
<p>Navigate to the <strong>Online Store &gt; </strong> <strong>Themes</strong> section. </p>

<p>
  <figure class="img-polaroid">
    <img alt="" src="img/theme.png">
  </figure>
</p
><p>Select the theme you want to add the  Password Page to.</p>
<p>Click on the <strong>Customize theme</strong> button to open the  theme editor page.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/cust.png">
  </figure>
</p
><p>Go to the<strong> General </strong>tab. Then click <strong>Layouts</strong>.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/layouts.png">
  </figure>
</p
>
<p>Tick a <strong>Show a link to the password page, to customize the display  of the page </strong>checkbox.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/show.png">
  </figure>

<p>Remember to SAVE the settings.</p>
<p>Press on the VIEW PASSWORD PAGE button to start the page  customization.</p>
<figure class="img-polaroid"><img alt="" src="img/button.png"></figure>
<h4><strong>Sections Tab </strong></h4>
<p>Go to the<strong> Sections</strong> section, double-click on <strong>Password</strong> tab to open the   page settings.</p>
<figure class="img-polaroid"><img alt="" src="img/set-20.png"></figure>
<a name="3">  </a>
<h5><strong>Upload the Logo</strong></h5>
<p>In this section you can set the password page Logo. </p>
<figure class="img-polaroid"><img alt="" src="img/set-1.png"></figure>

<p>The Logo will be scaled to 90px height.</p>
<p>You are able to Upload desired image from your computer or select image from your library. </p>
<figure class="img-polaroid"><img alt="" src="img/set-2.png"></figure>

<p>You are able to edit an image. Select<strong> Edit image</strong> from the <strong>Update</strong> dropdown.</p>
<p>Write a brief description of this image to improve search engine optimization (SEO) and accessibility for visually impaired customers.</p>
<figure class="img-polaroid"><img alt="" src="img/set-3.png"></figure>

<p>Remember to SAVE the settings</p>
<p>You can delete Logo image. In order to do this, press the<strong> Remove</strong> button.</p>
<p>
  <figure class="img-polaroid"><img alt="" src="img/set-4.png"></figure>
  
</p>
<a name="4">  </a>
<h5><strong>Add the page header</strong></h5>
<p>You can change the page header. Enter the text you want to display as the header in the <strong>Page header</strong> option textfield.</p>
<p>
  <figure class="img-polaroid"><img alt="" src="img/set-5.png"></figure>
  
</p>
<p>Remember to SAVE the settings.</p>
<a name="5">  </a>
<h5><strong>Add the text message</strong></h5>
<p>You can change the text message. Enter the text you want to display as the text message (under the page header) in the <strong>Text message </strong>option textfield.</p>
<p>
  <figure class="img-polaroid"><img alt="" src="img/set-6.png" /></figure>
</p>
<p>Remember to SAVE the settings.</p>
<a name="6">  </a>
<h5><strong>Change the background image</strong> </h5>
<p>You can change the background image for the password page.</p>
<p>
  <figure class="img-polaroid"><img alt="" src="img/set-7.png" /></figure>
</p>
<p>You are able to Upload desired image from your computer or select image from your library. For higher quality upload images wider than 2048 pixels.</p>
<p>You are able to edit an image. Select<strong> Edit image</strong> from the <strong>Update</strong> dropdown.</p>
<p>Remember to SAVE the settings</p>
<p>You can delete the background image. In order to do this, press the<strong> Remove</strong> button.</p>
<a name="7">  </a>
<h5><strong>Change the password label</strong> </h5>
<p>You can change the password label. Enter the text you want to display as the password label (above the password form) in the <strong>Password label </strong>option textfield.</p>
<p>
  <figure class="img-polaroid"><img alt="" src="img/set-8.png" /></figure>
</p>
<p>Remember to SAVE the settings.</p>
