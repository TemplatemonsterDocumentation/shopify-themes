<h2 class="item4">Password Page  Configuration</h2>

<p>Now you can  display the &quot;Site under maintenance&quot; page and let your  customers know that now there is some technical work going on and  that the website will soon become available. <br />
With the help of  this extension, you can change the appearance of this page. You can  specify the end time of the service work and display a timer to let  your clients know the timing of this work. The module allows you  display a subscription form for this page, and in the end of the work  you can send e-mail notifications to your clients.</p>
<p>Moreover, you can protect your online store with the password.</p>
