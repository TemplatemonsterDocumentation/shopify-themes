<ol class="marked-list">
  <li><strong>Section Height </strong> — select a suitable gallery section height from the option dropdown (Small/Medium/Large);
  </li></ol>
  <p>
  
  <strong>Content</strong>:</p>
  <p>
  <figure class="img-polaroid">
