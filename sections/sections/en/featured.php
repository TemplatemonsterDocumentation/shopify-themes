<h5>Let's review the Featured Products section
configuration:</h5>


<p>You may set the preferred title for the Featured products section. 
</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/featured_1.png">
  </figure>
</p>
<p>You may set the background color for the section, then there will also be some inner paddings.
</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/featured_3.png">
  </figure>
</p>
<p>The settings allow you to choose a collection to become ‘featured’ and the number of its products: 4 / 8 / 12 / 16 / 20.  There is also an option to define the desired product image size:
 <strong>Small</strong> (270x190), <strong>Medium  </strong> (270x270), <strong>Large</strong> (270x341) , and its alignment.
</p>

<p>
  <figure class="img-polaroid">
    <img alt="" src="img/featured_4.png">
  </figure>
  
</p>




<ol class="marked-list">
  <li><strong>Heading</strong> — add the title of the section;
  </li>
  <li><strong>Collection </strong>— choose the collection of products you want to display; </li>
  <li><strong>Show products</strong> —  	specify the number of products to show within the section;</li>
<li><strong>Images size</strong> —  	select a suitable product image size from the option dropdown (Small/Medium/Large);</li>
<li><strong>Images alignment</strong> — select a suitable product images alignment from the option dropdown (Top/Middle/Bottom).</li></ol>