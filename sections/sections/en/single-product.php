<h3>Single  Product</h3>
<p align="justify">This functionality enables you to show  a <strong>Single Product</strong> block on your site.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/single.png">
  </figure>
  
</p>
<h5>Let's review the Single Products section
configuration:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/single-2.png">
  </figure>
</p>
<ol class="marked-list">
  <li><strong>Product </strong>— choose the product to display;</li>
<li><strong>Image size</strong> —  	select a suitable product image size from the option dropdown (Small/Medium/Large);</li>
<li><strong>Image alignment</strong> — select a suitable product image alignment from the option dropdown (Top/Middle/Bottom);</li>
<li><strong>Show weight</strong> — enable the checkbox to show the product weight;</li>
<li><strong>Show SKU</strong> — enable the checkbox to show the product SKU;</li>
<li><strong>Show barcode</strong> — enable the checkbox to show the barcode;</li>
<li><strong>Show collections</strong> — enable the checkbox to show the collection;</li>
<li><strong>Show product types</strong> — enable the checkbox to show the product type;</li>
<li><strong>Show vendor</strong> —  enable the checkbox to show the product vendor;</li>
<li><strong>Show tags</strong> — enable the checkbox to show tags;</li>
<li><strong>Show social sharing buttons</strong> — enable the checkbox to show social sharing buttons;</li>
<li><strong>Show description</strong> — enable the checkbox to show description.</li>
</ol>
