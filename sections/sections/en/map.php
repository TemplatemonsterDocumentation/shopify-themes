</p>
<h5>Let's review the Google Map section
configuration:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/map-2.png">
  </figure>
</p>
<ol class="marked-list">
  <li><strong>Section height</strong> — select a suitable Google Map section height from the option dropdown (Small/Medium/Large); </li>
  <li><strong>Map address</strong> — enter the address you want to be marked on the map. Google maps will find the exact location;</li>
  <li>
    <div align="justify"><strong>API key — </strong>
         Google Api Key is necessary for the correct work of the map. You can receive and activate it by following the <a href="https://code.google.com/apis/console/" target="_blank">link</a>. If your store experiences more than 2,500 visitors a day, you'll need to generate both JavaScript and Geocoding API keys;</div>
  </li>
  <li><strong>Map style </strong>— choose the   appropriate Map Style from the option dropdown (Default style/Light/Dark/Pale Down/Apple maps/Midnight);</li>
<li><strong>Marker color — </strong>choose a color for the marker. You can select a color from the Color Palette; </li>
<li><strong>Show captions </strong>— enable the checkbox to show the heading, address and the office hours;</li>
<li><strong>Heading</strong> —  add the title of the section;</li>
<li><strong>Text address and hours</strong> —  input the text you want to display within the sections (address and the office hours);</li>
<li><strong>Show 'Get directions' button</strong>  — enable the checkbox to show<strong> Get directions</strong> button.  The site visitor  has the opportunity to  generate directions between the destinations (custom address and your store address). </li>
</ol>
