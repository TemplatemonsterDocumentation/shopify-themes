<h3>Language settings</h3>
<ul class="index-list">
	<li>On the template settings page, please click the <strong>Theme options</strong> link.</li>
	<li>Select Edit <strong>HTML/CSS</strong> in the drop-down.</li>
</ul>
<figure class="img-polaroid"><img alt="" src="img/10_lang_settings.png"></figure>

<p>On the new page, you will see the theme language settings editor:</p>
<figure class="img-polaroid"><img alt="" src="img/11_lang_editor_1.png"></figure>
<p>In this editor you can set up and edit translations for all the theme text. 
Our templates come with ready translations for 5 languages: English (enabled by default), German, French, Spanish, and Russian.</p>

<h4>Template text translation</h4>
<p>
	To translate template text, please complete the following steps:
</p>
<ul class="index-list">
	<li>Enter translations into text fields;</li>
	<li>Press the <strong>Save</strong> button at the top right of the page.</li>
</ul>
<figure class="img-polaroid"><img alt="" src="img/12_lang_editor_2.png"></figure>
<p>
	To switch the template language:
</p>
<ul class="index-list">
	<li>Press the <strong>Change theme language</strong> button.</li>
	<li>Select the language in the drop-down;</li>
	<li>Press the <strong>Save</strong> button.</li>
</ul>

<p class="alert alert-warning">
	<b>Attention!</b> Text of banners, sliders, and other configurable blocks can be also translated to other languages. Your store pages will display the text that is entered on language editor pages (instead of the text that is entered in template settings)!!!

</p>
<p class="alert alert-warning">Store content (products, categories, blog, text pages, etc.) is not a part of the template and cannot be translated with the help of template language settings editor. 
To translate this content, please use the corresponding apps or translate it manually.</p> 