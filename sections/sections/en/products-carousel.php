<h5>Let's review the  Products Carousel section
configuration:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/products-carousel-1.jpg">
  </figure>
  
</p>
<ol class="marked-list">
  <li><strong>Heading</strong> — add the title of the section.
  </li>

<figure class="img-polaroid">
    <img alt="" src="img/products-carousel-2.jpg">
  </figure>
  <li><strong>Title graphic element</strong> — upload the background image for the Title area. Maximum image size is 130 x 70. Click &quot;<strong>Edit</strong>&quot; to add the <strong>Image alt text</strong>.</li>

<figure class="img-polaroid">
    <img alt="" src="img/products-carousel-3.jpg">
  </figure>
  <li><strong>Caption </strong> — enter the text for the caption. The caption is displayed under the <strong>Heading</strong>.</li>
  <figure class="img-polaroid">
    <img alt="" src="img/products-carousel-4.jpg">
  </figure>

  <li><strong>Collection</strong> —  	select the products' collection to display in this section. You can add a new collection as well.</li>
  <figure class="img-polaroid">
    <img alt="" src="img/products-carousel-5.jpg">
  </figure>
  <li><strong>Carousel products count</strong> — specify a number of products to display. The carousel activates, in case you select <strong>8</strong>, <strong>12</strong>, <strong>16</strong> or <strong>20</strong> product to show in  <strong>One Row</strong> mode. In <strong>Two Rows</strong> mode the carousel is active for <strong>12</strong>, <strong>16</strong> and <strong>20</strong> values.</li>
  <figure class="img-polaroid">
    <img alt="" src="img/products-carousel-6.jpg">
  </figure>
  
  <li><strong>Carousel rows</strong> — you can set one or two rows carousel.</li>
  <li><strong>Carousel direction </strong>— choose the carousel direction. Note, the <strong>Two-way</strong> direction works for <strong>Two Rows</strong> mode only.</li>
   <figure class="img-polaroid">
    <img alt="" src="img/products-carousel-7.jpg">
  </figure>
  <li><strong>Image size</strong> — set the product images size. You can use <strong>Small</strong> (270x170 px), <strong>Medium</strong> (270x270 px) or <strong>Large</strong> (270x374 px) image size for the section.</li>
  <figure class="img-polaroid">
    <img alt="" src="img/products-carousel-8.jpg">
  </figure>
  <li><strong>Image alignment</strong> — select the image alignment from the option drop-down. </li>
  <figure class="img-polaroid">
    <img alt="" src="img/products-carousel-9.jpg">
  </figure>
</ol>
<p>Remember to <strong>Save</strong> the settings.</p>

<p>To remove the section press  the <strong>Delete Section</strong> button.</p>
