<h3>Collection List</h3>
<p align="justify">This functionality enables you to display a number of collections  in a grid with image, amount of products and VIEW ALL button.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/collection.png">
  </figure>
  
</p>
<h5>Let's review the Collection List section
configuration:</h5>
<p>The ‘Collection list’ section settings are the following:
</p>
<ol class="index-list">
  <li>The title of the section. </li>
  <li>Size of the collection image (Small 270x151px, Medium 270x255px, Large 270x360px) and its alignment.</li>
  <li>Text position: over the image or below it.</li>
  <li>The ‘View all’ button, leading to the listing page of all the collections.</li>
</ol>
<p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/collection-1.png">
  </figure>
  
</p>
<p>You can add up to 12 collection blocks (4 per row) into this section. To do this you just need to select the desired collection in the block settings.
</p>
<ol class="marked-list">
  <li><strong>Heading</strong> — add the title of the section;
  </li>
<li><strong>Images size</strong> —  	select a suitable collection image size from the option dropdown (Small/Medium/Large);</li>
<li><strong>Images alignment</strong> — select a suitable collection images alignment from the option dropdown (Top/Middle/Bottom);</li>
<li><strong>Text position</strong> — select a suitable text position from the option dropdown (Normal/Overlay);</li>
<li><strong>Show 'View all' button</strong> —   tick to show the View all button; </li>
<li><strong>Collection</strong> — choose the collection of products you want to display. </li></ol>

<p>To add a collection to display press the <strong>Add collection</strong> button. </p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/collection-4.png">
  </figure>
</p>
<p>Click on the <strong>Collection </strong>section.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/collection-5.png">
  </figure>
</p>
<p>Select desired collection from the option dropdown.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/collection-6.png">
  </figure>
</p>
<p>You  can display twelve collections maximum.</p>
