<h3>Image with Text Overlay</h3>
<p align="justify">It is possible for you to add image with the text on it to your website. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text-overlay.png">
  </figure>
  
</p>
<h5>Let's review the Image with Text Overlay section
  configuration:</h5>
<p>The settings allow you to choose the height of the section:  <strong>Small</strong>, <strong>Medium</strong>, <strong>Large</strong> and its display mode: fullwidth or boxed (in the container). It is possible to enable the parallax effect on scroll, choose the alignment for the text (right/left/center), enable the inverted text style, select the text background color, and its opacity.
Adding the extra blocks will let you compose the image description up to your desire.
</p>
<figure class="img-polaroid">
    <img alt="" src="img/img-text-overlay.png">
  </figure>
<p>The following blocks are available for selection:</p>
<ol class = "index-list">
  <li>H2 title, with an option of using one of 12 animation effects. </li>
  <li>H4 title</li>
  <li>H5 title </li>
  <li>Text</li>
  <li>The button with the link.</li>
</ol>



<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text-overlay-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li>
  <div align="justify"><strong>Image height</strong> — choose a suitable section  size from the option dropdown: Small(2048x256), Medium(2048x512), Large(2048x682);</div>
</li>
<li>
  <div align="justify"><strong>Image</strong> — choose a custom image for this section. You can upload image from your computer or select image from your library. The image should be more than 2048px wide to resize and crop properly;</div>
</li>
<li><strong>Images alignment</strong> — select a suitable  image alignment from the option dropdown (Top/Middle/Bottom);</li>
<li><strong>Parallax</strong> — enable the Checkbox  to apply parallax effect to the picture;</li>
<li><strong>Heading</strong> —  add the title of the section; </li>
<li><strong>Text</strong> —  	input the text you want to display within the section;</li>
<li><strong>Button label </strong>— specify the buttons' text;</li>
<li><strong>Button Link</strong> — specify a link to a certain Collection, Products, Pages, Blogs or Blog Post.</li></ol>
