<h3>Slideshow</h3>
<p align="justify">It is possible for you to add a slider to your website with text and button on the slides. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/slideshow.png">
  </figure>
  
</p>
<h5>Let's review the Slideshow section
  configuration:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/slideshow-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li>
  <div align="justify"><strong>Slideshow autoplay</strong> —  enable the Checkbox to apply the slideshow autoplay;</div>
</li>
<li><strong>Autoplay transition duration</strong> — defines slides' autoplay transition duration in seconds (5-10 seconds); </li>
<li><strong>Slide image size</strong> — select a suitable slider image size from the option dropdown (Small/Medium/Large);</li></ol>
<p><strong>Content</strong></p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/slideshow-2.png">
  </figure>
  
</p>
<ol class="marked-list">

<li>
  <div align="justify"><strong>Image</strong> — choose a custom image for this section. You can upload image from your computer or select image from your library;</div>
</li>
<li><strong>Images alignment</strong> — select a suitable slider image alignment from the option dropdown (Top/Middle/Bottom);</li>
<li><strong>Heading</strong> —  add the title of the slide; </li>
<li><strong>Subheading</strong> —  	input the text you want to display on the slide;</li>
<li><strong>Button  text </strong>— specify the button text;</li>
<li><strong>Button Link</strong> — specify a link to a certain Collection, Products, Pages, Blogs or Blog Post.</li></ol>
<p>To add a slide to  slider press <strong>Add image slide</strong> button. You can add<strong> five</strong> slides maximum.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-3.png">
  </figure>
</p>
<p>Click <strong>Image slide </strong>section.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-4.png">
  </figure>
</p>
<p align="justify">Upload desired image from your computer (press <strong>Upload image</strong> button) or select image from your library (press &quot;<strong>select from the library</strong>&quot;).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-5.png">
  </figure>
</p>
<p>Set an<strong> Image alignment</strong>: Top, Middle or Bottom.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-6.png">
  </figure>
</p>
<p>Specify  a<strong> Link </strong>to a certain Collection, Products, Pages, Blogs or Blog Post.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-7.png">
  </figure>
</p>
<p>Enter  the <strong>Caption</strong> (words printed above  a slide).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-8.png">
  </figure>
</p>
<p>You can delete slide from the<strong> Slideshow</strong> section (press <strong>Delete</strong> button).</p>