<h3>Text Columns with Images</h3>
<p align="justify">It is possible for you to add text columns with images to your website. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/text-images.png">
  </figure>
  
</p>
<h5>Let's review the Text columns with images section
  configuration:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/text-images-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li><strong>Heading</strong> —  add the title of the section;</li>
<li>
  <div align="justify"><strong>Image size</strong> — choose a suitable image  size from the option dropdown: Small(370x208), Medium(370x370), Large(370x493);</div>
</li>
<li>
  <div align="justify"><strong>Images alignment</strong> — select a suitable  images alignment from the option dropdown (Top/Middle/Bottom);</div>
</li>
<li><strong>Text alignment</strong> —  select a suitable  column text alignment from the option dropdown (Top/Middle/Bottom);</li></ol>
<p><strong>Content</strong></p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/text-images-2.png">
  </figure>
  
</p>
<ol class="marked-list">

<li>

  <div align="justify"><strong>Image</strong> — choose a custom image. You can upload image from your computer or select image from your library;</div>
</li>
<li><strong>Heading</strong> —  add the title which will be displayed under the image;</li>
<li><strong>Text </strong>—  add the text which will be displayed under the image;</li>
<li><strong>Button label </strong>— specify the buttons' text;</li>
<li><strong>Button Link</strong> — specify a link to a certain Collection, Products, Pages, Blogs or Blog Post.</li></ol>

<p>To add a new column to display press the <strong>Add column</strong> button. </p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-3.png">
  </figure>
</p>
<p>Click on the <strong> Add a title or tagline </strong>section.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-4.png">
  </figure>
</p>
<p align="justify">Upload desired image from your computer (press the <strong>Upload image</strong> button) or select image from your library (press &quot;select from the library&quot;).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-5.png">
  </figure>
</p>
<p>Enter the <strong>Heading</strong> and the <strong>Text</strong> which will displayed under the image.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-6.png">
  </figure>
</p>
<p>Specify a button text and set the link to a certain Collection, Products, Pages, Blogs or Blog Post.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-7.png">
  </figure>
</p

>
<p>You  can display <strong>six </strong>blocks maximum.</p>