<h3>Image with Text</h3>
<p align="justify">It is possible for you to add image with text to your website. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text.png">
  </figure>
  
</p>
<h5>Let's review the Image with Text section
  configuration:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li>
  <div align="justify"><strong>Image</strong> — choose a custom image for this section. You can upload image from your computer or select image from your library;</div>
</li>
<li><strong>Layout</strong> —  choose a proper layout type for the section (Image on left/Image on Right);</li>
<li><strong>Images size</strong> — select a suitable section image size from the option dropdown (Small/Medium/Large);</li>
<li><strong>Image alignment</strong> — select a suitable  image alignment from the option dropdown (Top/Middle/Bottom);</li>
<li><strong>Heading</strong> —  add the title of the section; </li>
<li><strong>Text</strong> —  	input the text you want to display within the section;</li>
<li><strong>Button label </strong>— specify the buttons' text;</li>
<li><strong>Button Link</strong> — specify a link to a certain Collection, Products, Pages, Blogs or Blog Post.</li></ol>
