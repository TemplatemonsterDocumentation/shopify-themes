<p align="justify">You can add <strong>Image</strong> and <strong>Text</strong> block.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom.png">
  </figure>
  
</p>
<p>Add <strong>Video</strong> block to your website.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-1.png">
  </figure>
  
</p>
<p>You also can add a block with the certain <strong>product</strong> to your website.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-2.png">
  </figure>
  
</p>
<p>A block with the certain<strong> collection</strong> can be placed on your website.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-3.png">
  </figure>
  
</p>
<p>You also can add a <strong>custom HTML</strong> block.</p>


<h5>Let's review the Custom Content section
  configuration:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-4.png">
  </figure>
  
</p>

<ol class="marked-list">
<li><strong>Heading</strong> —  add the title of the section;</li></ol>
<p><strong>Image </strong></p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-5.png">
  </figure>
  
</p><ol class="marked-list">


<li><strong>Image </strong>  — choose a custom image for this block. You can upload image from your computer or select image from your library; </li>
<li><strong>Container width</strong> — choose a suitable container width from the option dropdown (25/33/50/66/75/100%);</li>
<li>
  <div align="justify"><strong>Images alignment</strong> — select a suitable  image alignment from the option dropdown (Top/Middle/Bottom);</div>
</li></ol>
<p><strong>Text </strong></p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-6.png">
  </figure>
  
</p>
<ol class="marked-list">


<li><strong>Heading</strong> — add the title of the text block;</li>
<li>
  <div align="justify"><strong>Text</strong> —  input the text you want to display within the block. You can italicize text (press the &quot;I&quot; button) or  mark it in bold  (press the &quot;B&quot; button);</div>
</li>
<li><strong>Text alignment</strong> —  select a suitable  column text alignment from the option dropdown (Left/Centered/Right);</li></ol>
<p><strong>Video</strong></p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-7.png">
  </figure><ol class="marked-list">

<li>
  <div align="justify"><strong>YouYube video url</strong> —   this option allows you to specify custom URL for video from YouTube that has to be visible on the page;</div>
</li>
<li><strong>Container width</strong> — choose a suitable container width from the option dropdown (25/33/50/66/75/100%);</li></ol>
<p><strong>Product</strong></p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-8.png">
  </figure><ol class="marked-list">

<li><strong>Product</strong> —  choose the product to display;</li>
<li><strong>Container width</strong> — choose a suitable container width from the option dropdown (25/33/50/66/75/100%);</li></ol>
<p><strong>Collection</strong></p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-9.png">
  </figure><ol class="marked-list">

<li><strong>Collection</strong> — choose the collection of products you want to display;</li>
<li><strong>Container width</strong> — choose a suitable container width from the option dropdown (25/33/50/66/75/100%);</li></ol>
<p><strong>Custom HTML</strong></p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-10.png">
  </figure><ol class="marked-list">

<li><strong>HTML</strong> — enter the custom HTML in this field;</li>
<li><strong>Container width</strong> — choose a suitable container width from the option dropdown (25/33/50/66/75/100%).</li>
  </ol>
<p>To add a new <strong>Custom Block</strong>  press the <strong>Add content</strong> button. </p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/custom-11.png">
  </figure>
</p>
<p>Select desired custom section content type from the dropdown (Image/Text/Video/Product/Collection/Custom HTML).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/custom-12.png">
  </figure>
</p>
<p align="justify">Click the <strong> Content</strong> (Image/Talk about your brand/Video/Product/Collection/Custom HTML) section and configure the custom content block options.</p>
