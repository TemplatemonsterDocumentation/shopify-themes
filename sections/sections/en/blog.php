<h3>Blog Articles

</h3>
<p align="justify">This functionality enables you to display a number of blog posts from a certain blog (articles) in a grid with image, title, author name, publication date and VIEW ALL button.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/blog.png">
  </figure>
  
</p>
<h5>Let's review the Blog Articles section
configuration:</h5>
<p>This section settings allow you to set the title of the section, the background color, blog and the number of recent articles to display (3, 6 or 9), as well as the <strong>Small 370х208</strong>, <strong>Medium 370x308</strong> or <strong>Large 370x493рх</strong> image size. You can align the image by the top/bottom edges or by the center. The text can be placed in the usual way or over the image. You can also enable the author’s name display and the <strong>&quot;View all&quot;</strong>button which will lead to the blog page.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/blog-1.png">
  </figure>
  
</p>
<ol class="marked-list">
  <li><strong>Heading</strong> — add the title of the section;
  </li>
<li><strong>Blog </strong> — choose the collection of blog post you want to display; </li>
<li><strong>Show posts </strong> — set a number of article to show (3/6/9);</li>
<li><strong>Images size</strong> —  	select a suitable article image size from the option dropdown (Small/Medium/Large);</li>
<li><strong>Images alignment</strong> — select a suitable article images alignment from the option dropdown (Top/Middle/Bottom);</li>
<li><strong>Text position</strong> — select a suitable text position from the option dropdown (Normal/Overlay);</li>
<li><strong>Show author</strong> —  tick to show the author;</li>
<li><strong>Show 'View all' button</strong> —   tick to show the View all button. </li>
</ol>
