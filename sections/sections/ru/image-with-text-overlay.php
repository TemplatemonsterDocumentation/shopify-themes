<h3>Image with Text Overlay</h3>
<p align="justify">С помощью этой секции вы сможете добавить блок с изображением и текстом поверх изображения на страницу вашего сайта. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text-overlay.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Image with Text Overlay:</h5>
<p>В настройках возможно выбрать высоту секции <strong>Small</strong>, <strong>Medium</strong>, <strong>Large</strong> и ее вид: на весь экран или в контейнере. Имеется возможность  включить применение параллакс-эффекта при скролле, выбрать выравнивание подписи по левому краю, по правому краю или по центру, включить инвертированный стиль текста, выбрать цвет фона для подписи и его прозрачность.
Добавляя блоки, возможна компоновка подписи на изображении  по своему желанию. 
</p>
<figure class="img-polaroid">
    <img alt="" src="img/img-text-overlay.png">
  </figure>
<p>Доступны для выбора такие блоки:</p>
<ol class = "index-list">
  <li>Заголовок h2, на котором доступен один из 12 анимационных эффектов.</li>
  <li>Заголовок h4. </li>
  <li>Заголовок h5. </li>
  <li>Текст.</li>
  <li>Кнопка со ссылкой.</li>
</ol>




<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text-overlay-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li>
  <div align="justify"><strong>Image height</strong> — выберите наиболее подходящею высоту изображения: меленькое (2048x256), среднее (2048x512), большое(2048x682);</div>
</li>
<li>
  <div align="justify"><strong>Image</strong> — выберите изображение для данной секции. Загрузите изображение со своего компьютера, либо выберите изображение из библиотеки изображений; для лучшего качества изображений рекомендуется загружать изображения шире  2048 пикселей;</div>
</li>
<li><strong>Images alignment</strong> — выберите наиболее подходящее выравнивание изображения (Top/Middle/Bottom);</li>
<li><strong>Parallax</strong> — примените эффект параллакса для изображения. Для этого поставьте галочку напротив опции;</li>
<li><strong>Heading</strong> —  опция позволяет задать заголовок для данной секции; </li>
<li><strong>Text</strong> —  	введите текст, который ходите отображать в данной секции;</li>
<li><strong>Button label </strong>— задайте текст для кнопки;</li>
<li>
  <div align="justify"><strong>Button Link</strong> — задайте ссылку кнопки на определенную  Collection (коллекцию), Products (товар), Pages (странице), Blogs (категорию блога) или же Blog Post (статью блога).</div>
</li>
</ol>
