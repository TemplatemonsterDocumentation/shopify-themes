<h3>Blog Articles

</h3>
<p align="justify">Возможности этой секции позволяют отображать заданное количество статей с определенной категории (коллекции) блога на вашем сайте. Статьи блога отображаются с главным изображением статьи, заголовком, частью текста,  именем автора, датой публикации и с кнопкой  READ MORE.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/blog.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции  Blog Articles:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/blog-1.png">
  </figure>
  
</p>
<ol class="marked-list">
  <li><strong>Heading</strong> — опция позволяет задать заголовок для данной секции;
  </li>
<li><strong>Blog </strong> — выберите коллекцию (категорию) статьи(статью) которой ходите отобразить на странице вашего сайта; </li>
<li><strong>Show posts </strong> — выберите количество статей для отображения (3/6/9);</li>
<li><strong>Images size</strong> —  	выберите наиболее подходящий размер главного изображения блога (Small/Medium/Large);</li>
<li><strong>Images alignment</strong> — выберите наиболее подходящее выравнивания для изображений блога  (Top/Middle/Bottom);</li>
<li>
  <div align="justify"><strong>Text position</strong> — выберите положения текста: под изображением или  с наложением текста поверх изображения (Normal/Overlay);</div>
</li>
<li><strong>Show author</strong> —  отметьте поле галочкой, чтобы отображать автора статьи;</li>
<li><strong>Show 'View all' button</strong> —     отметьте поле галочкой, чтобы отображать кнопку &quot;View all&quot;. </li>
</ol>
