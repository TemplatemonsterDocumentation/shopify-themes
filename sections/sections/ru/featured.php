<h3>Featured Product</h3>
<p align="justify">С помощью этой секции вы сможете добавить секцию   &quot;Featured products&quot; на ваш сайт. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/featured.png">
  </figure>
  

</p>
<h5>Более подробно рассмотрим настройки секции Featured Products:</h5>
<p>В настройках секции возможно изменить заголовок секции. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/featured_1.png">
  </figure>
</p>
<p>При выборе цвета фона в секции также будут добавлены внутренние отступы.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/featured_3.png">
  </figure>
</p>
<p>Также в настройках необходимо выбрать коллекцию, из которой будут добавлены товары и их количество: 4, 8, 12, 16, 20, выбрать размер изображения товара <strong>Small</strong> (270x190), <strong>Medium  </strong> (270x270), <strong>Large</strong> (270x341) и его выравнивание.
</p>

<p>
  <figure class="img-polaroid">
    <img alt="" src="img/featured_4.png">
  </figure>
  
</p>
<ol class="marked-list">
  <li><strong>Heading</strong> — опция позволяет задать заголовок для данной секции;
  </li>
  <li><strong>Collection </strong>—    выберите коллекцию, продукты которой хотите отображать в данной секции; </li>
  <li><strong>Show products</strong> —  	задайте колличество продуктов, которое хотите отображать в данной секции;</li>
<li><strong>Images size</strong> —  	выберите наиболее подходящий размер  для изображения продукта  (Small/Medium/Large);</li>
<li>
  <div align="justify"><strong>Images alignment</strong> — выберите наиболее подходящее выравнивание для изображений продуктов (Top/Middle/Bottom).</div>
</li>
</ol>