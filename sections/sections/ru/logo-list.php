<h3>Logo List</h3>
<p align="justify">С помощью этой секции вы сможете добавить секцию с изображением логотипов (Logos) со ссылкой на определенную  Collection (коллекцию), Products (товар), Pages (странице), Blogs (категорию блога) или же Blog Post (статью блога).</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/logo-1.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Logo List:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/logo-2.png">
  </figure>
  
</p>
<ol class="marked-list">
  <li><strong>Heading</strong> —   опция позволяет задать заголовок для данной секции;
  </li>
<li><strong>Images size</strong> —  	выберите наиболее подходящий размер   изображений (Small/Medium/Large);</li>
<li><strong>Images alignment</strong> — выберите наиболее подходящее выравнивание изображений (Top/Middle/Bottom);</li>
<p><strong>Content</strong></p></ol>
<ol class="marked-list">
<p>
