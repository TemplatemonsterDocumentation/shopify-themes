<li>
  <div align="justify"><strong>Image</strong> — выберите изображение для логотипа. Загрузите изображение со своего компьютера, либо выберите изображение из библиотеки изображений;</div>
</li>
<li>
  <div align="justify"><strong>Link </strong>— задайте ссылку изображения на определенную  Collection (коллекцию), Products (товар), Pages (странице), Blogs (категорию блога) или же Blog Post (статью блога).</div>
</li>
</ol>

<p>Для того, чтобы добавить  новый логотип, нажмите кнопку  <strong>Add logo</strong>. </p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/logo-4.png">
  </figure>
</p>
<p>Перейдите к секции настройки логотипа (Logo).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/logo-5.png">
  </figure>
</p>
<p align="justify">Загрузите желаемое изображение с вашего компьютера  (для этого, нажмите кнопку <strong>Upload image</strong>) или же выберите изображение из библиотеки изображений  (нажмите &quot;<strong>select from the library</strong>&quot;).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/logo-6.png">
  </figure>
</p>
<p align="justify">Определите ссылку  изображения<strong> </strong>на определенную коллекцию (Collection), товар (Product), страницу (Pages), категорию блога (Blogs) или же статью блога (Blog Post).</p>
