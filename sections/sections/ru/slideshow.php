<h3>Slideshow</h3>
<p align="justify">С помощью этой секции вы сможете отображать слайдер с кнопками и текстом на слайде на странице вашего сайта. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/slideshow.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Slideshow:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/slideshow-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li>
  <div align="justify"><strong>Slideshow autoplay</strong> —  включите или отключите опцию автоматического переключения слайдов;</div>
</li>
<li><strong>Autoplay transition duration</strong> — выберите  значение задержки в секундах между переключением слайдов (от 5 до 10 сек.); </li>
<li><strong>Slide image size</strong> — выберите наиболее подходящий размер  изображения слайда (Small/Medium/Large);</li></ol>
<p><strong>Content</strong></p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/slideshow-2.png">
  </figure>
  
</p>
<ol class="marked-list">

<li>
  <div align="justify"><strong>Image</strong> — выберите изображение для данной секции. Загрузите изображение со своего компьютера, либо выберите изображение из библиотеки изображений;</div>
</li>
<li><strong>Images alignment</strong> — выберите наиболее подходящее выравнивание изображения слайда (Top/Middle/Bottom);</li>
<li><strong>Heading</strong> —  опция позволяет задать заголовок слайда; </li>
<li><strong>Subheading</strong> —  	введите подзаголовок, который будет отображаться на слайде;</li>
<li><strong>Button  text </strong>— задайте текст кнопки;</li>
<li>
  <div align="justify"><strong>Button Link</strong> — задайте ссылку  слайда на определенную  Collection (коллекцию), Products (товар), Pages (странице), Blogs (категорию блога) или же Blog Post (статью блога).</div>
</li>
</ol>
<p align="justify">Для того, чтобы добавить новый слайд нажмите кнопку <strong>Add image slide</strong>. В слайдер вы можете добавить 5 слайдов максимум.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-3.png">
  </figure>
</p>
<p>Перейдите в секцию настройки отображения слайда (<strong>Image slide</strong>).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-4.png">
  </figure>
</p>
<p align="justify">Загрузите желаемое изображение с вашего компьютера  (для этого, нажмите кнопку <strong>Upload image</strong>) или же выберите изображение из библиотеки изображений  (нажмите &quot;<strong>select from the library</strong>&quot;).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-5.png">
  </figure>
</p>
<p>Задайте нужное значение выравнивания изображения (<strong>Image alignment</strong>: Top, Middle or Bottom).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-6.png">
  </figure>
</p>
<p>Определите ссылку слайда на определенную коллекцию (Collection), товар (Product), страницу (Pages), категорию блога (Blogs) или же статью блога (Blog Post).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-7.png">
  </figure>
</p>
<p>Введите текст для надписи (<strong>Caption</strong>); текст, который будет отображаться поверх изображения.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/slideshow-8.png">
  </figure>
</p>
<p>В случае необходимости вы сможете удалить слайд. Для этого нажмите кнопку <strong>Delete</strong>.</p>
