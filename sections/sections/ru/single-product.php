<h3>Single  Product</h3>
<p align="justify">С помощью этой секции вы сможете отображать блок<strong> </strong>с одним товаром на странице вашего сайта.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/single.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Single Products:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/single-2.png">
  </figure>
</p>
<ol class="marked-list">
  <li><strong>Product </strong>— выберите продукт, страницу описания которого ходите отображать в данной секции;</li>
<li><strong>Image size</strong> —  	выберите наиболее подходящий размер  для изображения товара (Small/Medium/Large);</li>
<li><strong>Images alignment</strong> — выберите наиболее подходящее выравнивание изображения товара (Top/Middle/Bottom);</li>
<li><strong>Show weight</strong> — включите или отключите отображения поля с указанием веса товара;</li>
<li><strong>Show SKU</strong> — включите или отключите отображения блока с указанием  артикула товара;</li>
<li><strong>Show barcode</strong> — включите или отключите отображения блока с указанием   штрихкода товара;</li>
<li><strong>Show collections</strong> —  включите или отключите отображения блока с указанием   коллекции товара;</li>
<li><strong>Show product types</strong> — включите или отключите отображения блока с указанием   типа товара;</li>
<li><strong>Show vendor</strong> —  включите или отключите отображения блока с указанием   поставщика товара;</li>
<li><strong>Show tags</strong> — включите или отключите отображения блока с указанием   меток (тегов) данного  товара;</li>
<li><strong>Show social sharing buttons</strong> — включите или отключите отображения кнопок &quot;Поделиться&quot;;</li>
<li><strong>Show description</strong> — включите или отключите отображения блока описания товара.</li>
</ol>
