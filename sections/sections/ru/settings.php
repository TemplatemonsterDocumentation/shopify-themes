<h3>Настройки темы (Theme Settings)</h3>
<p>При клике на кнопку <strong>Customize theme</strong> откроется страница редактора настроек, с помощью которого вы
    можете изменять внешний вид и содержимое шаблона. Это <strong>Live editor</strong>: вы можете отслеживать изменения
    внешнего вида вашего магазина без перезагрузки страницы.
</p>
