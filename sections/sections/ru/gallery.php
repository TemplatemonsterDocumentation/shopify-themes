<h3>Gallery</h3>
<p align="justify">С помощью секции <strong>Gallery</strong>, добавьте галерею изображений или лукбук на страницу вашего сайта. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/gallery.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Gallery:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/gallery-1.png">
  </figure>
  
</p>
<ol class="marked-list">
  <li><strong>Section Hight </strong> — выберите наиболее подходящею высоту секции галереи (Small/Medium/Large);
  </li></ol>
  <p>
  
<strong>Content</strong>:</p>
  <p>
  <figure class="img-polaroid">
    <img alt="" src="img/gallery-10.png">
  </figure>
  
</p>
<ol class="marked-list">
<li>
  <div align="justify"><strong>Image</strong> —  выберите изображение для галереи. Загрузите изображение со своего компьютера, либо выберите изображение из библиотеки изображений; для лучшего качества изображений рекомендуется загружать изображения шире  2048 пикселей;</div>
</li>
<li>
  <div align="justify"><strong>Images alignment</strong> — выберите наиболее подходящее выравнивание для изображений галереи (Top/Middle/Bottom);</div>
</li>
<li>
  <div align="justify"><strong>Link</strong> — задайте ссылку для изображения на определенную  Collection (коллекцию), Products (товар), Pages (странице), Blogs (категорию блога) или же Blog Post (статью блога);</div>
</li>
<li><strong>Caption </strong>(optional) —   введите текст<strong> надписи</strong> (текст, который будет наложен на изображение). </li></ol>

<p align="justify">Для того, чтобы добавить новое изображение для галереи, нажмите кнопку <strong>Add image</strong>. В галерею вы можете включать <strong>4</strong> изображения максимум.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/gallery-4.png">
  </figure>
</p>
<p>Перейдите к секции настройки изображения (<strong>Image</strong>).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/gallery-5.png">
  </figure>
</p>
<p align="justify">Загрузите желаемое изображение с вашего компьютера  (для этого, нажмите кнопку <strong>Upload image</strong>) или же выберите изображение из библиотеки изображений  (нажмите &quot;<strong>select from the library</strong>&quot;).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/gallery-6.png">
  </figure>
</p>
<p>Задайте нужное значение выравнивания изображения (<strong>Image alignment</strong>: Top, Middle or Bottom).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/gallery-7.png">
  </figure>
</p>
<p align="justify">Определите ссылку изображения на определенную коллекцию (Collection), товар (Product), страницу (Pages), категорию блога (Blogs) или же статью блога (Blog Post).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/gallery-8.png">
  </figure>
</p>
<p>Введите текст для надписи (<strong>Caption</strong>); текст, который будет отображаться поверх изображения.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/gallery-9.png">
  </figure>
</p>
<p>В случае необходимости вы сможете удалить добавленное изображение. Для этого нажмите кнопку  <strong>Delete</strong>.</p>
<p> <strong>Пример: </strong></p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/gallery-11.png">
  </figure>
  </p>

