  <li>
    <div align="justify"><strong>Image</strong> — 2 images;</div>
  </li>
  <li><strong>Images alignment</strong> </li>
  <li>Picture #1 —  Bottom;</li>
  <li>Picture #2 —  Bottom;</li>
  <li><strong>Link</strong> — no;</li>
  <li><strong>Caption</strong> —   no. </li>
</ol>
