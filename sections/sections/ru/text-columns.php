<h3>Text Columns with Images</h3>
<p align="justify">С помощью этой секции вы сможете добавлять колонки с текстом и изображением на странице вашего сайта. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/text-images.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Text columns with images:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/text-images-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li><strong>Heading</strong> —    опция позволяет задать заголовок для данной секции;</li>
<li>
  <div align="justify"><strong>Image size</strong> — выберите наиболее подходящий размер  для изображения: маленький(370x208), средний(370x370), большой(370x493);</div>
</li>
<li>
  <div align="justify"><strong>Images alignment</strong> — выберите наиболее подходящее выравнивание для изображения (Top/Middle/Bottom);</div>
</li>
<li><strong>Text alignment</strong> —   выберите наиболее подходящее выравнивание для текста (Top/Middle/Bottom);</li></ol>
<p><strong>Content</strong></p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/text-images-2.png">
  </figure>
  
</p>
<ol class="marked-list">

<li>

  <div align="justify"><strong>Image</strong> — выберите изображение для данной секции. Загрузите изображение со своего компьютера, либо выберите изображение из библиотеки изображений;</div>
</li>
<li><strong>Heading</strong> —  опция позволяет задать заголовок, который будет отображаться под изображением;</li>
<li><strong>Text </strong>—  введите текст, который будет отображаться под изображением;</li>
<li><strong>Button label </strong>— задайте текст кнопки;</li>
<li>
  <div align="justify"><strong>Button Link</strong> — задайте ссылку   на определенную  Collection (коллекцию), Products (товар), Pages (странице), Blogs (категорию блога) или же Blog Post (статью блога).</div>
</li>
</ol>

<p>Для того, чтобы добавить новую колонку, нажмите кнопку  <strong>Add column</strong>. </p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-3.png">
  </figure>
</p>
<p>Перейдите в секцию настройки колонки (<strong>Add a title or tagline</strong>).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-4.png">
  </figure>
</p>
<p align="justify">Загрузите желаемое изображение с вашего компьютера  (для этого, нажмите кнопку <strong>Upload image</strong>) или же выберите изображение из библиотеки изображений  (нажмите &quot;<strong>select from the library</strong>&quot;).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-5.png">
  </figure>
</p>
<p>Задайте <strong>заголовок</strong> (Heading)  и <strong>текст</strong> (Text), который будет отображаться под изображением.</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-6.png">
  </figure>
</p>
<p align="justify">Задайте текст кнопки и определите ссылку изображения на определенную коллекцию (Collection), товар (Product), страницу (Pages), категорию блога (Blogs) или же статью блога (Blog Post).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/text-images-7.png">
  </figure>
</p

