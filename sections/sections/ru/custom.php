<h3>Custom Content</h3>
<p align="justify">С помощью этой секции вы сможете добавить пользовательский контент на ваш сайт.</p>
<p align="justify">На страницу вашего сайта вы можете добавить блок с <strong>Изображением</strong> (Image) или <strong>Текстом</strong> (Text).</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom.png">
  </figure>
  
</p>
<p>Добавьте блок с<strong> Видео</strong> (Video) на страницу вашего сайта.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-1.png">
  </figure>
  
</p>
<p>Вы можете отображать блок с определенным <strong>продуктом</strong> (product).</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-2.png">
  </figure>
  
</p>
<p>Добавьте на страницу вашего сайта блок с определенной <strong>коллекцией</strong> (collection).</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-3.png">
  </figure>
  
</p>
<p>Вы также можете добавить блок <strong>пользовательского HTML</strong>.</p>


<h5>Более подробно рассмотрим настройки секции Custom Content:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-4.png">
  </figure>
  
</p>

<ol class="marked-list">
<li><strong>Heading</strong> —  опция позволяет задать заголовок для данной секции;</li></ol>
<p><strong>Image </strong>(Изображение)</p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-5.png">
  </figure>
  
</p><ol class="marked-list">


<li>
  <div align="justify"><strong>Image </strong>  — выберите изображение для блока. Загрузите изображение со своего компьютера, либо выберите изображение из библиотеки изображений; </div>
</li>
<li><strong>Container width</strong> — выберите наиболее подходящею ширину контейнера для изображения (25/33/50/66/75/100%);</li>
<li>
  <div align="justify"><strong>Images alignment</strong> —  выберите наиболее подходящее выравнивание для изображения (Top/Middle/Bottom);</div>
</li></ol>
<p><strong>Text </strong>(Текст)</p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-6.png">
  </figure>
  
</p>
<ol class="marked-list">


<li><strong>Heading</strong> — опция позволяет задать заголовок для данного блока;</li>
<li>
  <div align="justify"><strong>Text</strong> —  задайте текст, который хотите отображать в этом блоке. Вы можете выделять текст курсивом (нажмите кнопку &quot;I&quot;) или выделять жирным шрифтом   (нажмите кнопку &quot;B&quot;);</div>
</li>
<li><strong>Text alignment</strong> —    выберите наиболее подходящее выравнивание для текста блока (Left/Centered/Right);</li></ol>
<p><strong>Video</strong> (Видео)</p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-7.png">
  </figure><ol class="marked-list">

<li>
  <div align="justify"><strong>YouYube video url</strong> —   в этом поле, задайте URL того YouTube видео, которое хотите добавить на ваш сайт;</div>
</li>
<li><strong>Container width</strong> — выберите наиболее подходящею ширину контейнера для видео (25/33/50/66/75/100%);</li></ol>
<p><strong>Product</strong> (Продукт)</p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-8.png">
  </figure><ol class="marked-list">

<li><strong>Product</strong> —  выберите продукт, который хотите отображать в данном блоке;</li>
<li><strong>Container width</strong> — выберите наиболее подходящею ширину контейнера для продукта (25/33/50/66/75/100%);</li></ol>
<p><strong>Collection</strong> (Коллекция)</p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-9.png">
  </figure><ol class="marked-list">

<li><strong>Collection</strong> —   выберите коллекцию, которую хотите отображать в данном блоке;</li>
<li><strong>Container width</strong> — выберите наиболее подходящею ширину контейнера для коллекции (25/33/50/66/75/100%);</li></ol>
<p><strong>Custom HTML</strong> (Блок с пользовательским HTML)</p><p>
  <figure class="img-polaroid">
    <img alt="" src="img/custom-10.png">
  </figure><ol class="marked-list">

<li><strong>HTML</strong> — введите  пользовательский  HTML в это поле;</li>
<li><strong>Container width</strong> — выберите наиболее подходящею ширину контейнера (25/33/50/66/75/100%).</li>
  </ol>
<p>Для того, чтобы добавить новый блок с пользовательским контентом, нажмите кнопку <strong>Add content</strong>. </p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/custom-11.png">
  </figure>
</p>
<p align="justify">Выберите тип контента, который хотите добавить на страницу вашего сайта (Image/ Text/ Video/ Product/ Collection/ Custom HTML).</p>
<p>
    <figure class="img-polaroid">
      <img alt="" src="img/custom-12.png">
  </figure>
</p>
<p align="justify">Перейдите в секцию настройки выбранного контента<strong> </strong>(Image/Talk about your brand/Video/Product/Collection/Custom HTML), настройте параметры отображения блока.</p>
