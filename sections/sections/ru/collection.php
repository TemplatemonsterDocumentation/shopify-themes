<h3>Collection List</h3>
<p align="justify">Возможности этой секции позволяют отображать заданное количество категорий в сетке с изображением категории, количеством товарных единиц в категории и кнопкой VIEW ALL.</p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/collection.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Collection List:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/collection-1.png">
  </figure>
  
</p>
<ol class="marked-list">
  <li><strong>Heading</strong> — опция позволяет задать заголовок для данной секции;
  </li>
<li><strong>Images size</strong> —  	выберите наиболее подходящий размер главного изображения коллекции (Small/Medium/Large);</li>
<li><strong>Images alignment</strong> — выберите наиболее подходящее выравнивания для изображений коллекций (Top/Middle/Bottom);</li>
<li><strong>Text position</strong> — выберите положения текста: под изображением или  с наложением текста поверх изображения (Normal/Overlay); </li>
<li><strong>Show 'View all' button</strong> —  отметьте поле галочкой, чтобы отображать кнопку View all; </li>
<li><strong>Collection</strong> — выберите коллекцию, которую хотите отображать. </li></ol>

<p>Для того, чтобы добавить коллекцию нажмите кнопку  <strong>Add collection</strong>. </p>
