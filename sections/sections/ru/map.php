<h5>Более подробно рассмотрим настройки секции Google Map:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/map-2.png">
  </figure>
</p>
<ol class="marked-list">
  <li><strong>Section height</strong> — выберите наиболее подходящею высоту секции (Small/Medium/Large); </li>
  <li><strong>Map address</strong> — введите адрес, который будет помещен маркером на карте. Сервис Google карты найдет точное местоположение на карте;</li>
  <li>
    <div align="justify"><strong>API key — </strong>
         Google Api Key важен для корректной работы карты. Вы можете получить и активировать свой Google Api Key по <a href="https://code.google.com/apis/console/" target="_blank">ссылке</a>. Если ваш сайт обладает посещаемостью свыше  2,500 посетителей на день, то вам необходимо будет получить оба ключа — JavaScript и Geocoding API;</div>
  </li>
  <li>
    <div align="justify"><strong>Map style </strong>— выберите наиболее подходящий, из предложенных, стиль карты (Default style/ Light/ Dark /Pale Down/ Apple maps/ Midnight);</div>
  </li>
<li><strong>Marker color — </strong>выберите цвет для маркера. Вы можете выбрать нужный цвет из цветовой палитры; </li>
<li><strong>Show captions </strong>— включите или отключите показ заголовка, адреса и часов работы;</li>
<li><strong>Heading</strong> —  опция позволяет задать заголовок для данной секции;</li>
<li><strong>Text address and hours</strong> —  введите адрес, название организации и часы работы;</li>
<li>
  <div align="justify"><strong>Show 'Get directions' button</strong>  — опция позволяет отображать кнопку <strong> Get directions</strong>.  Нажав на кнопку <strong> Get directions</strong> любой посетитель вашего сайта сможет получить карту маршрука (от места, которое определит посетитель до места помеченного на карте, как адрес вашей организации). </div>
</li>
</ol>
