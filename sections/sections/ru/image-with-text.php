<h3>Image with Text</h3>
<p align="justify">С помощью этой секции вы сможете добавить блок с изображением и текстом на страницу вашего сайта. </p>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text.png">
  </figure>
  
</p>
<h5>Более подробно рассмотрим настройки секции Image with Text:</h5>
<p>
  <figure class="img-polaroid">
    <img alt="" src="img/image-with-text-1.png">
  </figure>
  
</p>

<ol class="marked-list">
<li>
  <div align="justify"><strong>Image</strong> — выберите изображение для данной секции. Загрузите изображение со своего компьютера, либо выберите изображение из библиотеки изображений; </div>
</li>
<li>
  <div align="justify"><strong>Layout</strong> —  выберите наиболее подходящий макет для данной секции: изображение слева (Image on left), изображение справа (Image on Right);</div>
</li>
<li><strong>Images size</strong> — выберите наиболее подходящий размер  для изображения (Small/Medium/Large);</li>
<li><strong>Image alignment</strong> — выберите наиболее подходящее выравнивание изображения (Top/Middle/Bottom);</li>
<li><strong>Heading</strong> —  опция позволяет задать заголовок для данной секции; </li>
<li><strong>Text</strong> —  	введите текст, который ходите отображать в данной секции;</li>
<li><strong>Button label </strong>— задайте текст кнопки;</li>
<li>
  <div align="justify"><strong>Button Link</strong> — задайте ссылку кнопки на определенную  Collection (коллекцию), Products (товар), Pages (странице), Blogs (категорию блога) или же Blog Post (статью блога).</div>
</li>
</ol>
