<h4>Google fonts for the users from China:</h4>
<p align="justify">If you are experiencing problems with Google fonts display on the pages of your store, replace <strong>fonts.googleapis.com</strong>
    with <strong>fonts.useso.com</strong> in URLs of the fonts.</p>
<p>For example, the link to Open Sans font will look like this:</p>
<ol class="index-list">
    <li>before replacement
        <br>
        <code>&lt;link href='//fonts.googleapis.com/css?family=Open+Sans' rel='stylesheet' type='text/css'&gt;</code>
    </li>

    <li>after replacement
        <br>
        <code>&lt;link href='//fonts.useso.com/css?family=Open+Sans' rel='stylesheet' type='text/css'&gt;</code>
    </li>
</ol>
