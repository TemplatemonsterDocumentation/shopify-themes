<h3>Загрузка шаблона</h3>
<ol class="index-list">
    <li>Перейдите в раздел <strong>Online store</strong> > <strong>Themes</strong>. <figure class="img-polaroid"><img alt="" src="img/shopify/01_navigate.png"> </figure>По умолчанию там содержится исходная тема;<figure class="img-polaroid"><img alt="" src="img/shopify/02_navigate.png"> </figure></li>
    
    <li>Нажмите кнопку <strong>Upload theme</strong> в верхней части страницы справа;
        <figure class="img-polaroid"><img alt="" src="img/01_themes.png"></figure>
    </li>
    <li>В появившемся диалоговом окне нажмите <strong>Browse (Обзор);</strong> 
      <figure class="img-polaroid"><img alt="" src="img/02_upload.png"></figure><!-- и откройте папку шаблона на своём
        компьютере. -->
        <figure class="img-polaroid"><img alt="" src="img/02_browse.png"></figure>
    </li>
  <li>Откройте папку шаблона на своём компьютере и выберите файл шаблона, он имеет вид <strong> theme###.zip;</strong>.
        <figure class="img-polaroid"><img alt="" src="img/03_choose.png"></figure>
    </li>
    <li>
      <div align="justify">Нажмите кнопку <strong>Upload</strong>. Через несколько секунд загруженный шаблон появится в перечне тем вашего
        интернет-магазина.
      </div>
    </li>
</ol>