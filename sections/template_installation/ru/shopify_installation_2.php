<h3>Загрузка шаблона</h3>
<ol class="index-list">
    <li>Перейдите в меню <strong>Themes</strong>. По умолчанию там содержится дефолтная тема.</li>
    <li>Нажмите кнопку <strong>Upload a theme</strong> в верхней части страницы справа.
        <figure class="img-polaroid"><img alt="" src="img/01_themes.png"></figure>
    </li>
    <li>В появившемся диалоговом окне нажмите <strong>Browse (Обзор)</strong> и откройте папку шаблона на своём
        компьютере.
        <figure class="img-polaroid"><img alt="" src="img/02_browse.png"></figure>
    </li>
    <li>Выберите файл шаблона, он имеет вид <strong>theme###.zip</strong>.
        <figure class="img-polaroid"><img alt="" src="img/03_choose.png"></figure>
    </li>
    <li>Нажмите кнопку <strong>Upload</strong>. Через несколько секунд загруженный шаблон появится в перечне тем вашего
        интернет-магазина.
    </li>
</ol>