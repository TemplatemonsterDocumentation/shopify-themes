<h3>Theme installation</h3>
<ol class="index-list">
    <li>Navigate to <strong>Themes</strong>. <figure class="img-polaroid"><img alt="" src="img/shopify/01_navigate.png"> </figure>It contains a default Shopify theme.<figure class="img-polaroid"><img alt="" src="img/shopify/02_navigate.png"> </figure></li>
    <li>Click <strong>Upload a theme</strong> button in the upper right corner.
        <figure class="img-polaroid">
            <img alt="" src="img/01_themes.png">
        </figure>
    </li>
    <li>Click <strong>Browse</strong> in the pop-up window and open the template folder on your computer.
        <figure class="img-polaroid">
            <img alt="" src="img/02_browse.png">
        </figure>
    </li>
    <li>Select <strong>theme###.zip</strong> file.
        <figure class="img-polaroid">
            <img alt="" src="img/03_choose.png">
        </figure>
    </li>
    <li>Click <strong>Upload</strong> button. In a few seconds the new theme will show up in the list of the available
        themes for your online store.
    </li>
</ol>