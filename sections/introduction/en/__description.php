<h2 class="item1">Introduction</h2>

<h6>Thank you for purchasing a Shopify template. This documentation consists of several parts and shows you the entire
    process of setting up and administering a Shopify store from scratch.</h6>


<h3>What is Shopify platform?</h3>

<p><a href="//www.shopify.com/">Shopify</a> is an online eCommerce platform for creating online stores. The system is
    SaaS-based (software as a service).</p>

<h3>What is SaaS?</h3>

<p>It is a model of providing services where the developer takes care of maintaining and administering the software,
    providing customers with the online application for temporary use. So, you do not need to look for hosting, to
    install and to configure the softwarethe developer does all this. You're getting a ready-made online shop in just
    a few clicks.</p>

<h3>What is Shopify template?</h3>

<p>Shopify template is a theme for your online store. You can easily change the appearance of your online shop by
    installing a new theme in a few easy steps. The template includes an archive with files that can be modified to your
    needs.</p>

