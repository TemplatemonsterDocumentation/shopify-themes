<h4><strong>Linklist</strong></h4>
<p>Linklists help your customers navigate around your online store. </p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/footer-7.png" /> </figure>
</p>
<p align="justify">Enter the menu item <strong>title</strong>. You can set a link specify a link to a certain <strong>Collection</strong>, <strong>Product</strong>, <strong>Pages</strong>, <strong>Blogs</strong> or <strong>Blog Post</strong>. Choose the linklist from the option dropdown.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-51.png" /> </figure>
</p>
<p><strong>How to create a new linklist </strong></p>
<p>To create a new <strong>menu with a linklist</strong>, navigate to the site <strong>administration panel</strong> &gt;<strong> Online Store</strong>.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-19.png" /> </figure>
</p>
<p>Go to the <strong>Navigation</strong> section. </p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-20.png" /> </figure>
</p>
<p>Press the <strong>Add menu</strong> button. </p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-21.png" /> </figure>
</p>
<p>Specify the menu desctiption. Enter the <strong>name </strong>(&quot;New Collection&quot;) of the menu.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-22.png" /> </figure>
</p>
<p>You cam add menu item  to the menu. In order to do this, press <strong>Add menu items</strong> button.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-24.png" /> </figure>
</p>
<p>Enter the <strong>name </strong> of the menu item.</p>
<p>You can set a link to the:</p>
<ul class="marked-list">
  <li><strong>Home Page</strong>;</li>
  <li><strong>Collection</strong>. You need to choose the certain <strong>collection</strong> from the <strong>Select a collection</strong> option dropdown;</li>
  <li><strong>All Collections</strong>;</li>
  <li><strong>Product</strong>.  You need to choose the certain <strong>product</strong> from the <strong>Select a product</strong> option dropdown;</li>
  <li><strong>All Products</strong>;</li>
  <li><strong>Page</strong>. You need to choose the certain <strong>page</strong> from the <strong>Select a page</strong> option dropdown;</li>
  <li><strong>Blog</strong>. You need to choose the certain<strong> blog </strong>from the <strong>Select a blog</strong> option dropdown;</li>
  <li><strong>Search Page</strong>;</li>
  <li><strong>Web address</strong>. Enter the<strong> custom</strong> link.</li>
</ul>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-23.png" /> </figure>
</p>
<p>Click the <strong>Save menu </strong>button  to apply changes. </p>
