<h4>How to add a new footer content block</h4>
<p>To add a new content block  in the footer, press <strong>Add content</strong> button. You can display<strong> six </strong>menu items maximum.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-60.png" /></figure>
</p>
<p align="justify">Select the new footer content block <strong>type</strong> from the option dropdown (<strong>Linklist</strong>/<strong> Contacts block/ Facebook block</strong>/ <strong>Twitter block/ Newsletter form/ Social link</strong>).</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/footer-10.png" /></figure>
</p>
<p>Click the <strong>Linklist</strong>/<strong> Contacts block/ Facebook block</strong>/ <strong>Twitter block/ Newsletter form/ Social link</strong> block settings section.</p>
<p>Configure the footer block settings. Click the <strong>Save </strong>button  to apply changes. </p>
<p>You can delete the footer block. In order to do this, click the <strong>delete</strong> button. </p>
