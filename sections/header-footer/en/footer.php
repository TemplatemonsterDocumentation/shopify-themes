<h4><strong>Contacts block</strong></h4>
<p>Contacts block allows you to display the section with specified address, the office hours, phone number in the footer.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/footer-8.png" /> </figure>
</p>
<ul class="marked-list">
  <li><strong>Block width</strong> — select a suitable contact block width from the option dropdown (25%, 33%, 50%, 66%, 75%, 100%);</li>
  <li><strong>Block title</strong> —  add the title of the contacts block; </li>
  <li><strong>Contacts</strong> — input the text you want to display within the sections (address, the office hours, phone number).</li>
</ul>
