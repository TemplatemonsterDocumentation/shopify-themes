<h3>Template Editor</h3>
<p>If you click this option, the online editor of the template will show up. You can edit layout files (files with
    extension <b>.liquid</b>), scripts (<b>javascript</b>), styles (<b>css</b>) and settings file (<b>settings.html</b>).
</p>
