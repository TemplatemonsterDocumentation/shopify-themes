<h4><strong>Megamenu</strong></h4>

<p align="justify">Our templates include the Megamenu widget which helps to build a powerful and functional main menu of your store. The
    widget has many options that are available in the corresponding section. Menu includes 6 <strong>Items</strong>,
    each of which is configured in the corresponding sub-section. One can add a label with the text (<strong>Item
        badge</strong>) to each item:</p>

<ul class="marked-list">
    <li>
      <div align="justify">the usual custom link, without submenu (<strong>Simple link</strong>);</div>
    </li>
    <li>
      <div align="justify">each column of your mega menu are able to display an individual linklist (<strong>Link + linklist</strong>). Menus, or link lists, help your customers navigate around your online store;</div>
    </li>
    <li>each column of your mega menu are able to display the main menu link  with only four linklists (<strong>Link + 4 linklist</strong>s);</li>
    <li>
      <div align="justify">product listing (<strong>Link + product listing</strong>).  Category is chosen by the user, the option to enable/disable the drop-down sub-menu with the list of
      the latest entries (4 latest products) is also available;</div>
    </li>
  <li>
      <div align="justify">blog (<strong>Link + blog listing</strong>). Blog is chosen by the user, the option to enable/disable the drop-down sub-menu with the list of
        the latest entries (4 latest posts) is also available. </div>
  </li>
</ul>
