<h4>Как добавить новый блок в футер сайта</h4>
<p>Для того, чтобы добавить новый блок в футер, нажмите кнопку  <strong>Add content</strong> button. В футере вашего сайта вы можете отображать только 6 блоков максимум.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-60.png" /></figure>
</p>
<p align="justify">Выберите тип блока из выпадающего списка опции (<strong>Linklist</strong>/<strong> Contacts block/ Facebook block</strong>/ <strong>Twitter block/ Newsletter form/ Social link</strong>).</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/footer-10.png" /></figure>
</p>
<p>Перейдите к секции настройки блока (<strong>Linklist</strong>/<strong> Contacts block/ Facebook block</strong>/ <strong>Twitter block/ Newsletter form/ Social link</strong>).</p>
<p align="justify">Настройте параметры блока желаемым образом. Нажмите кнопку  <strong>Save </strong>для применения настроек отображения футера. </p>
<p>Вы можете удалить любой блок из футера, нажав кнопку <strong>delete</strong> в секции настройки блока. </p>
