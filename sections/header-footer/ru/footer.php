<h4><strong>Каталог ссылок (Linklist</strong>)</h4>
<p>Отображение каталога ссылок способно значительно облегчить навигацию по сайтуLinklists help your customers navigate around your online store. </p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/footer-7.png" /> </figure>
</p>
<p align="justify">Введите наименование пункта меню в поле <strong>title</strong>. Вы можете задать ссылку этого пункта меню на определенную коллекцию, продукт, страницу, категорию блога и пост (<strong>Collection</strong>, <strong>Product</strong>, <strong>Pages</strong>, <strong>Blogs</strong> or <strong>Blog Post</strong>). Выберите нужный каталог ссылок из выпадающего списка опции<strong> Choose linklist</strong>.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-51.png" /> </figure>
</p>
<p><strong>Как создать новый каталог ссылок (linklist) </strong></p>
<p align="justify">Для того, чтобы создать новый каталог ссылок, перейдите на <strong>главную страницу администрирования сайтом</strong> &gt;<strong> Online Store</strong>.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-19.png" /> </figure>
</p>
<p>Перейдите к разделу <strong>Navigation</strong>. </p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-20.png" /> </figure>
</p>
<p>Нажмите кнопку <strong>Add menu</strong>. </p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-21.png" /> </figure>
</p>
<p>Добавьте описание к каталогу ссылок. Задайте имя каталогу в поле <strong>name </strong>(&quot;New Collection&quot;) of the menu.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-22.png" /> </figure>
</p>
<p>Для добавления ссылок к каталогу, нажмите кнопку <strong>Add menu items</strong>.</p>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-24.png" /> </figure>
</p>
<p>Задайте имя ссылки в поле <strong>name </strong> (имя ссылки будет отображаться в субменю).</p>
<p>Вы можете добавить ссылку на:</p>
<ul class="marked-list">
  <li><strong>Home Page</strong> (Главную страницу сайта);</li>
  <li>
    <div align="justify"><strong>Collection</strong> (страницу коллекции товаров). Вам необходимо выбрать определенную <strong>коллекцию</strong> с выпадающего списка опции <strong>Select a collection</strong>;</div>
  </li>
  <li><strong>All Collections</strong> (страницу с перечнем всех коллекций товаров);</li>
  <li>
    <div align="justify"><strong>Product</strong> (страницу Товара).  Вам необходимо выбрать определенный<strong> товар</strong> с выпадающего списка опции<strong> Select a product</strong>;</div>
  </li>
  <li><strong>All Products</strong> (станица с перечнем всех товаров);</li>
  <li>
    <div align="justify"><strong>Page</strong> (страницу). Вам необходимо выбрать определенную<strong> страницу </strong>с выпадающего списка опции<strong> Select a page option</strong>;</div>
  </li>
  <li><strong>Blog</strong> (страницу категории блога). Вам необходимо выбрать определенную <strong>категорию блога </strong>с выпадающего списка опции<strong> Select a blog</strong>;</li>
  <li><strong>Search Page</strong> (страницу поиска);</li>
  <li>
    <div align="justify"><strong>Web address</strong> (веб-адрес). Задайте веб-адрес ссылки. Адрес, на который будет переходить пользователь при нажатии на ссылку.</div>
  </li>
</ul>
<p>
  <figure class="img-polaroid"> <img alt="" src="img/header-23.png" /> </figure>
